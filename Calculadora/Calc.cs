using System;
using System.Collections.Generic;
using System.Text;

namespace Calculadora
{
    public static class Calc
    {
        /// <summary>
        /// Soma dois inteiros
        /// </summary>
        /// <param name="numb1"> Primeiro inteiro</param>
        /// <param name="numb2"> Segund inteiro</param>
        /// <returns>Soma dos valores.</returns>
        public static long SomaInteiros( long numb1, long numb2)
        {
            return numb1 + numb2;
        }
    }
}
